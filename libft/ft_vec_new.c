#include "libft.h"

void	ft_vec_new(t_vec *v)
{
	v = (t_vec *)malloc(sizeof(t_vec *));
	v->cap = VEC_CAP;
	v->str = ft_strnew(VEC_CAP - 1);
	v->len = 0;
}
