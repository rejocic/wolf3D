#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		ft_putchar('-');
		if (n < -9)
			ft_putnbr(-1 * (n / 10));
		n = -1 * (n % 10);
	}
	else if (n >= 10)
		ft_putnbr(n / 10);
	ft_putchar((n % 10) + '0');
}
